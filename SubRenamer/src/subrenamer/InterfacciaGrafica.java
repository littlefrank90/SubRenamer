    private void finestraScegliFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finestraScegliFileActionPerformed

        if(evt.getActionCommand().equals(javax.swing.JFileChooser.APPROVE_SELECTION)){
            File directorySelezionata = finestraScegliFile.getSelectedFile();
            
            //se non c'Ã¨ specificato un file prende il percorso della directory in cui si trova il file jar
            String percorsoFile = ".";
            
            if (directorySelezionata.isDirectory()) {
                percorsoFile = directorySelezionata.getPath();
            } else if(directorySelezionata.isFile()){
                percorsoFile = directorySelezionata.getAbsolutePath();
            }
            
            Rinominatore rinominatore = new Rinominatore(percorsoFile);
        }
        else if(evt.getActionCommand().equals(javax.swing.JFileChooser.CANCEL_SELECTION)){
            System.exit(0);
        }
    }//GEN-LAST:event_finestraScegliFileActionPerformed

